char	*ft_strcapitalize(char *str)
{
	int		i;
	char	a;

	i = 1;
	if (str[0] >= 'a' && str[0] <= 'z')
	{
		str[0] = str[0] - 32;
	}
	while (str[i] != '\0')
	{
		a = str[i - 1];
		if (!((a >= 'A' && a <= 'Z') || (a >= 'a' && a <= 'z')
			|| (a >= '0' && a <= '9')))
		{
			if (str[i] >= 'a' && str[i] <= 'z')
				str[i] = str[i] - 32;
		}
		if (((a >= 'A' && a <= 'Z') || (a >= 'a' && a <= 'z'))
			&& (str[i] >= 'A' && str[i] <= 'Z'))
		{
			str[i] = str[i] + 32;
		}
		i++;
	}
	return (str);
}
