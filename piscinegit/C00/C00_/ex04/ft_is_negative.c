#include <unistd.h>

void	ft_is_negative(int n)
{
	char put;

	if (n >= 0)
	{
		put = 'P';
		write(1, &put, 1);
	}
	else if (n < 0)
	{
		put = 'N';
		write(1, &put, 1);
	}
}
