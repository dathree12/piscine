int	ft_is_prime(int nb)
{
	long long	i;
	long long	lnb;

	i = 2;
	lnb = nb;
	if (nb <= 1)
		return (0);
	while (i * i <= lnb)
	{
		if (lnb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
